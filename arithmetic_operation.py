#Arithmetic Operation

first_number = 3


#addition

first_number += 3
print(first_number)


#subtraction

first_number -= 3
print(first_number)

#multiplication

first_number *= 3
print(first_number)

#Division

first_number /= 3
print(first_number)

#Floor division

first_number //= 3
print(first_number)

#Modulus

first_number %= 3
print(first_number)
